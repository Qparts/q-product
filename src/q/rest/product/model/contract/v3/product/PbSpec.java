package q.rest.product.model.contract.v3.product;

public class PbSpec {

    private String key;
    private String keyAr;
    private String value;
    private String valueAr;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getKeyAr() {
        return keyAr;
    }

    public void setKeyAr(String keyAr) {
        this.keyAr = keyAr;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getValueAr() {
        return valueAr;
    }

    public void setValueAr(String valueAr) {
        this.valueAr = valueAr;
    }
}
